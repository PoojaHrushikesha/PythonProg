# Declare variables of different types

integer_variable = 10
float_variable = 3.14
string_variable = "Hello, Python!"
list_variable = [1, 2, 3]
dictionary_variable = {"key": "value"}
boolean_variable = True

# Print the type of each variable using type()
print(f"Type of integer_variable: {type(integer_variable)}")
print(f"Type of float_variable: {type(float_variable)}")
print(f"Type of string_variable: {type(float_variable)}")
print(f"Type of list_variable: {type(list_variable)}")
print(f"Type of dictionary_variable: {type(dictionary_variable)}")
print(f"Type of boolean_variable: {type(boolean_variable)}")
